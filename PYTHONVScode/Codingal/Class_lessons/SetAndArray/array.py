# an array is a collection of elements of the same type, stored at contiguous memory locations. 
# Although Python doesn't have a built-in array data type like some other programming 
# languages, you can use lists to achieve similar functionality.
# Creating a list (array)
numbers = [1, 2, 3, 4, 5]
# Accessing elements
first_number = numbers[0]  # Retrieves the first element (1)
second_number = numbers[1]  # Retrieves the second element (2)
# Modifying elements
numbers[0] = 10  # Changes the first element to 10

